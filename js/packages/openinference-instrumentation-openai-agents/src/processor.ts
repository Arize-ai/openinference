// ─── Custom span ─────────────────────────────────────────────────────────────

/**
 * Extracts attributes from {@link CustomSpanData}, serialising the user-defined
 * data dict as JSON output.
 */
function getCustomAttributes(data: CustomSpanData): Attributes {
  const attrs: Attributes = {};
  if (isRecord(data.data) && Object.keys(data.data).length > 0) {
    const json = safelyJSONStringify(data.data);
    if (json) {
      attrs[OUTPUT_VALUE] = json;
      attrs[OUTPUT_MIME_TYPE] = MimeType.JSON;
    }
  }
  return attrs;
}

// ─── Status ──────────────────────────────────────────────────────────────────

function setSpanStatus(otelSpan: OtelSpan, span: Span<SpanData>, eventTime?: HrTime): void {
  if (span.error) {
    let description = span.error.message || "Unknown error";
    if (span.error.data) {
      const dataStr = isString(span.error.data)
        ? span.error.data
        : (safelyJSONStringify(span.error.data) ?? "");
      if (dataStr) {
        description = `${description}: ${dataStr}`;
      }
    }
    otelSpan.recordException({ message: description }, eventTime);
    otelSpan.setStatus({ code: SpanStatusCode.ERROR, message: description });
  } else {
    otelSpan.setStatus({ code: SpanStatusCode.OK });
  }
}

// ─── Processor ───────────────────────────────────────────────────────────────

/**
 * A {@link TracingProcessor} that bridges `@openai/agents` tracing events to
 * OpenTelemetry spans following the OpenInference semantic conventions.
 *
 * Register it with the agents SDK via
 * {@link OpenAIAgentsInstrumentation.instrument} (preferred) or directly with
 * `setTraceProcessors` / `addTraceProcessor`.
 *
 * @see https://github.com/Arize-ai/openinference/blob/main/spec/semantic_conventions.md
 */
export class OpenInferenceTracingProcessor implements TracingProcessor {
  private enabled = true;
  private readonly oiTracer: OITracer;
  private readonly maxRootSpansInFlight: number;

  /** trace_id → root OTel span */
  private readonly rootSpans = new Map<string, OtelSpan>();
  /** span_id → OTel span */
  private readonly otelSpans = new Map<string, OtelSpan>();
  /**
   * Tracks in-flight handoffs so that when the destination agent's span starts
   * we can record `graph.node.parent_id` to visualise multi-agent flows.
   *
   * Key: `${to_agent}:${trace_id}`. Value: `from_agent` name.
   *
   * `Map` preserves insertion order, so we use it as an LRU bound by
   * {@link MAX_HANDOFFS_IN_FLIGHT} to guard against unbounded growth when
   * handoff spans never get a follow-up agent span.
   */
  private readonly reverseHandoffs = new Map<string, string>();
  /** trace_id → input/output lifted from LLM spans for the root span. */
  private readonly traceIO = new Map<string, LiftedIO>();
  /**
   * SDK parent span_id → input/output lifted from child LLM spans, applied to
   * the parent AGENT span when it ends. `Map` insertion order is used as an
   * LRU bound by {@link MAX_AGENT_IO_IN_FLIGHT}.
   */
  private readonly agentIO = new Map<string, LiftedIO>();
  /** trace_id values whose trace start happened under suppressed tracing. */
  private readonly suppressedTraceIds = new Set<string>();
  /** span_id values skipped at start, so their end event can be ignored safely. */
  private readonly suppressedSpanIds = new Set<string>();

  constructor({
    tracerProvider,
    traceConfig,
    maxRootSpansInFlight,
  }: OpenInferenceTracingProcessorOptions = {}) {
    const baseTracer = (tracerProvider ?? trace).getTracer(INSTRUMENTATION_NAME, VERSION);
    this.oiTracer = new OITracer({ tracer: baseTracer, traceConfig });
    this.maxRootSpansInFlight = normalizeMaxRootSpansInFlight(maxRootSpansInFlight);
  }

  async onTraceStart(agentTrace: Trace): Promise<void> {
    if (!this.enabled) return;

    if (isTracingSuppressed(context.active())) {
      this.suppressedTraceIds.add(agentTrace.traceId);
      boundIdSet(this.suppressedTraceIds, MAX_SUPPRESSED_IDS_RETAINED);
      return;
    }

    const rootSpan = this.oiTracer.startSpan(agentTrace.name, {
      attributes: {
        [OPENINFERENCE_SPAN_KIND]: OpenInferenceSpanKind.AGENT,
        // `llm.system` identifies the agents SDK, which is an OpenAI product.
        // In chat_completions mode the underlying model may be served by a
        // non-OpenAI provider (e.g. DeepSeek); `llm.provider` is set per-span
        // where it can be determined.
        [LLM_SYSTEM]: LLMSystem.OPENAI,
      },
    });
    this.rootSpans.set(agentTrace.traceId, rootSpan);
    this.evictOldestRootSpans();
  }

  async onTraceEnd(agentTrace: Trace): Promise<void> {
    this.suppressedTraceIds.delete(agentTrace.traceId);
    const io = this.traceIO.get(agentTrace.traceId);
    this.traceIO.delete(agentTrace.traceId);
    const rootSpan = this.rootSpans.get(agentTrace.traceId);
    if (!rootSpan) return;
    rootSpan.setAttributes(liftedIOAttributes(io));
    rootSpan.setStatus({ code: SpanStatusCode.OK });
    rootSpan.end();
    this.rootSpans.delete(agentTrace.traceId);
  }

  async onSpanStart(span: Span<SpanData>): Promise<void> {
    if (!span.startedAt) return;
    if (!this.enabled) return;

    if (this.shouldSuppressSpan(span)) {
      this.suppressedSpanIds.add(span.spanId);
      boundIdSet(this.suppressedSpanIds, MAX_SUPPRESSED_IDS_RETAINED);
      return;
    }

    const parentSpan = span.parentId
      ? this.otelSpans.get(span.parentId)
      : this.rootSpans.get(span.traceId);

    const parentContext = parentSpan
      ? trace.setSpan(context.active(), parentSpan)
      : context.active();

    const otelSpan = this.oiTracer.startSpan(
      getSpanName(span),
      {
        startTime: isoToHrTime(span.startedAt),
        attributes: {
          [OPENINFERENCE_SPAN_KIND]: getSpanKind(span.spanData),
          // See onTraceStart: the agents SDK is the "system"; the provider may differ.
          [LLM_SYSTEM]: LLMSystem.OPENAI,
        },
      },
      parentContext,
    );

    this.otelSpans.set(span.spanId, otelSpan);
    this.evictOldestOtelSpans();
  }

  async onSpanEnd(span: Span<SpanData>): Promise<void> {
    if (this.suppressedSpanIds.delete(span.spanId)) return;

    const otelSpan = this.otelSpans.get(span.spanId);
    if (!otelSpan) return;
    this.otelSpans.delete(span.spanId);

    // Some span types (e.g. handoff) don't have their final name resolved
    // until the span ends, so refresh it here.
    otelSpan.updateName(getSpanName(span));

    let attrs: Attributes = {};
    const data = span.spanData;
    switch (data.type) {
      case "generation":
        attrs = getGenerationAttributes(data);
        this.recordLiftedIO(span, getGenerationLiftedIO(data));
        break;
      case "function":
        attrs = getFunctionAttributes(data);
        break;
      case "response":
        attrs = getResponseAttributes(data);
        this.recordLiftedIO(span, getResponseLiftedIO(data));
        break;
      case "mcp_tools":
        attrs = getMCPListToolsAttributes(data);
        break;
      case "custom":
        attrs = getCustomAttributes(data);
        break;
      case "handoff":
        attrs = getHandoffAttributes(data);
        this.recordHandoff(data, span.traceId);
        break;
      case "agent":
        this.applyAgentGraphAttributes(otelSpan, data, span.traceId);
        attrs = liftedIOAttributes(this.agentIO.get(span.spanId));
        this.agentIO.delete(span.spanId);
        break;
      case "guardrail":
        otelSpan.setAttribute(TOOL_NAME, data.name);
        otelSpan.setAttribute("guardrail.triggered", data.triggered);
        break;
      case "speech":
      case "speech_group":
      case "transcription":
        // These span types carry no additional attributes in the current
        // OpenInference spec. Listed explicitly for exhaustiveness.
        break;
    }

    const endTime = span.endedAt ? isoToHrTime(span.endedAt) : undefined;
    otelSpan.setAttributes(attrs);
    setSpanStatus(otelSpan, span, endTime);

    otelSpan.end(endTime);
    this.endDanglingRootSpanOnTopLevelAgentError(span, endTime);
  }

  async shutdown(_timeout?: number): Promise<void> {
    // The OTel TracerProvider owns flushing/shutdown — nothing to do here.
  }

  async forceFlush(): Promise<void> {
    // The OTel TracerProvider owns flushing — nothing to do here.
  }

  enable(): void {
    this.enabled = true;
  }

  disable(): void {
    this.enabled = false;
    for (const span of this.otelSpans.values()) {
      span.setStatus({ code: SpanStatusCode.OK });
      span.end();
    }
    for (const span of this.rootSpans.values()) {
      span.setStatus({ code: SpanStatusCode.OK });
      span.end();
    }
    this.otelSpans.clear();
    this.rootSpans.clear();
    this.suppressedSpanIds.clear();
    this.suppressedTraceIds.clear();
    this.reverseHandoffs.clear();
    this.traceIO.clear();
    this.agentIO.clear();
  }

  // ─── Private helpers ───────────────────────────────────────────────────────

  /**
   * Accumulates an LLM span's input/output so it can be surfaced on the
   * enclosing AGENT spans: the trace's root span and, when the LLM span has a
   * parent (the per-agent span), that parent.
   */
  private recordLiftedIO(span: Span<SpanData>, io: LiftedIO): void {
    if (io.input === undefined && io.output === undefined) return;

    if (this.rootSpans.has(span.traceId)) {
      let traceEntry = this.traceIO.get(span.traceId);
      if (!traceEntry) {
        traceEntry = {};
        this.traceIO.set(span.traceId, traceEntry);
      }
      mergeLiftedIO(traceEntry, io);
    }

    if (span.parentId != null && this.otelSpans.has(span.parentId)) {
      let agentEntry = this.agentIO.get(span.parentId);
      if (!agentEntry) {
        agentEntry = {};
        this.agentIO.set(span.parentId, agentEntry);
        while (this.agentIO.size > MAX_AGENT_IO_IN_FLIGHT) {
          const oldest = this.agentIO.keys().next().value;
          if (oldest === undefined) break;
          this.agentIO.delete(oldest);
        }
      }
      mergeLiftedIO(agentEntry, io);
    }
  }

  private evictOldestOtelSpans(): void {
    while (this.otelSpans.size > MAX_SPANS_IN_FLIGHT) {
      const oldestSpanId = this.otelSpans.keys().next().value;
      if (oldestSpanId === undefined) break;

      const otelSpan = this.otelSpans.get(oldestSpanId);
      otelSpan?.setStatus({ code: SpanStatusCode.OK });
      otelSpan?.end();
      this.otelSpans.delete(oldestSpanId);
      this.agentIO.delete(oldestSpanId);
    }
  }

  private evictOldestRootSpans(): void {
    while (this.rootSpans.size > this.maxRootSpansInFlight) {
      const oldestTraceId = this.rootSpans.keys().next().value;
      if (oldestTraceId === undefined) break;

      const rootSpan = this.rootSpans.get(oldestTraceId);
      rootSpan?.setStatus({ code: SpanStatusCode.OK });
      rootSpan?.end();
      this.rootSpans.delete(oldestTraceId);
      this.traceIO.delete(oldestTraceId);
      this.suppressedTraceIds.delete(oldestTraceId);
    }
  }

  /**
   * Ends the root span when a top-level agent span ends with an error.
   *
   * The SDK does not emit `onTraceEnd` when a run aborts (e.g. a guardrail
   * tripwire throws), which would otherwise leave the root span dangling
   * until eviction. This assumes the error ends the trace: if the caller
   * catches the error and runs another top-level agent under the same trace,
   * that agent's spans will no longer be parented under this root span.
   */
  private endDanglingRootSpanOnTopLevelAgentError(
    span: Span<SpanData>,
    endTime: HrTime | undefined,
  ): void {
    if (!span.error || span.spanData.type !== "agent") return;

    const rootSpan = this.rootSpans.get(span.traceId);
    if (!rootSpan) return;

    const rootSpanId = rootSpan.spanContext().spanId;
    if (span.parentId != null && span.parentId !== rootSpanId) return;

    rootSpan.setAttributes(liftedIOAttributes(this.traceIO.get(span.traceId)));
    this.traceIO.delete(span.traceId);
    setSpanStatus(rootSpan, span, endTime);
    rootSpan.end(endTime);
    this.rootSpans.delete(span.traceId);
  }

  private shouldSuppressSpan(span: Span<SpanData>): boolean {
    return (
      isTracingSuppressed(context.active()) ||
      this.suppressedTraceIds.has(span.traceId) ||
      (span.parentId != null && this.suppressedSpanIds.has(span.parentId))
    );
  }

  /**
   * Records a handoff so that the destination agent's span can later be
   * linked to the source agent via `graph.node.parent_id`.
   */
  private recordHandoff(data: HandoffSpanData, traceId: string): void {
    if (!data.to_agent || !data.from_agent) return;
    const key = `${data.to_agent}:${traceId}`;
    // Re-insert to refresh LRU position
    this.reverseHandoffs.delete(key);
    this.reverseHandoffs.set(key, data.from_agent);
    while (this.reverseHandoffs.size > MAX_HANDOFFS_IN_FLIGHT) {
      const oldest = this.reverseHandoffs.keys().next().value;
      if (oldest === undefined) break;
      this.reverseHandoffs.delete(oldest);
    }
  }

  /**
   * Annotates an agent span with graph-node identifiers, including a parent
   * pointer if this agent was reached via a handoff.
   */
  private applyAgentGraphAttributes(
    otelSpan: OtelSpan,
    data: AgentSpanData,
    traceId: string,
  ): void {
    otelSpan.setAttribute(GRAPH_NODE_ID, data.name);
    const key = `${data.name}:${traceId}`;
    const parentNode = this.reverseHandoffs.get(key);
    if (parentNode) {
      otelSpan.setAttribute(GRAPH_NODE_PARENT_ID, parentNode);
      this.reverseHandoffs.delete(key);
    }
  }
}
